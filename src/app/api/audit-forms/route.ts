export async function GET(req: NextRequest) {
  try {
    const type = req.nextUrl.searchParams.get("type");
    if (!type) {
      return NextResponse.json({ error: "نوع مرکز الزامی است" }, { status: 400 });
    }

    const categoryTitle = TYPE_MAP[type] || null;
    if (!categoryTitle) {
      return NextResponse.json({ error: "فرم نظارتی برای این نوع مرکز تعریف نشده", form: null });
    }

    const formsData = JSON.parse(
      readFileSync(join(process.cwd(), "src/data/audit-forms.json"), "utf-8")
    );

    const form = formsData.find((f: { title: string }) => f.title === categoryTitle);

    if (!form) {
      return NextResponse.json({ error: "فرم نظارتی یافت نشد", form: null });
    }

    return NextResponse.json({ form });
  } catch (error) {
    console.error("Audit forms error:", error);
    return NextResponse.json({ error: "خطا در دریافت فرم نظارتی" }, { status: 500 });
  }
}