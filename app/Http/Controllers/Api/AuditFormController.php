    public function __invoke(Request $request)
    {
        $type = $request->query('type');

        if (!$type) {
            return response()->json(['form' => null, 'message' => 'نوع مرکز مشخص نشده است.']);
        }

        $categoryTitle = self::TYPE_MAP[$type] ?? null;

        if (!$categoryTitle) {
            return response()->json(['form' => null, 'message' => 'فرم نظارتی برای نوع «' . $type . '» تعریف نشده.']);
        }

        $formsPath = base_path('src/data/audit-forms.json');

        if (!file_exists($formsPath)) {
            return response()->json(['form' => null, 'message' => 'فایل فرم‌ها یافت نشد.']);
        }

        $forms = json_decode(file_get_contents($formsPath), true);

        if (!is_array($forms)) {
            return response()->json(['form' => null, 'message' => 'خطا در خواندن فایل فرم‌ها.']);
        }

        $matchedForm = collect($forms)->firstWhere('title', $categoryTitle);

        if (!$matchedForm) {
            return response()->json(['form' => null, 'message' => 'فرم نظارتی برای نوع «' . $type . '» تعریف نشده.']);
        }

        return response()->json(['form' => $matchedForm]);
    }
